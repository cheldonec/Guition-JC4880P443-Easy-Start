// main/applications/sys_app_time_sync/time_sync_app.h
#ifndef APP_TIME_SYNC_H
#define APP_TIME_SYNC_H

#include "esp_err.h"
#include <time.h>  // ← для time_t

// ============================================================================
// 🌍 ЧАСОВЫЕ ПОЯСА (стандартные)
// ============================================================================

/**
 * @brief Москва (UTC+3) — без перехода на летнее время (с 2014 г.)
 * @note Используется формат `MSK-3` (смещение вперёд на 3 часа от UTC)
 */
#define TZ_MSK     "MSK-3"

/**
 * @brief Всемирное время (UTC+0)
 * @note Не использует летнее время
 */
#define TZ_UTC     "UTC+0"

/**
 * @brief Лондон (GMT/BST) — зимой GMT+0, летом BST+1 (UTC+1)
 * @note Формат `GMT0BST` — стандартный для Unix TZ: `std offset dst [offset]`
 */
#define TZ_LON     "GMT0BST"

/**
 * @brief Нью-Йорк (EST/EDT) — зимой EST-5 (UTC-5), летом EDT-4 (UTC-4)
 * @note Формат `EST+5EDT` — смещение назад на 5 часов + летнее время
 */
#define TZ_NYC     "EST+5EDT"

/**
 * @brief Токио (JST) — японское стандартное время (UTC+9), без летнего
 * @note Формат `JST-9` — смещение вперёд на 9 часов
 */
#define TZ_TKY     "JST-9"

/**
 * @brief Сидней (AEST/AEDT) — зимой AEST+10 (UTC+10), летом AEDT+11 (UTC+11)
 * @note Формат `AEST-10AEDT` — смещение назад на 10 часов + летнее время
 */
#define TZ_SYD     "AEST-10AEDT"

/**
 * @brief Значение по умолчанию — Москва (TZ_MSK)
 * @details Используется, если пользователь не указал часовой пояс и NVS пуст
 */
#define TZ_DEFAULT TZ_MSK


// ============================================================================
// 📋 ОСНОВНЫЕ ФУНКЦИИ API
// ============================================================================

/**
 * @brief Инициализация модуля синхронизации времени
 * @param timezone Часовой пояс в формате `TZ` (например, "MSK-3", "EST+5EDT")
 *        - Если `NULL` — будет загружен из NVS (ключ `"time"/"tz"`)
 *        - Если NVS пуст — будет использован `TZ_DEFAULT`
 * @return
 *        - `ESP_OK` — инициализация успешна
 *        - `ESP_ERR_NO_MEM` — нехватка памяти
 *        - `ESP_FAIL` — другие ошибки
 *
 * @details
 * 1. Загружает часовой пояс из NVS (если есть) или использует переданный параметр
 * 2. Сохраняет часовой пояс в NVS (если был передан новый)
 * 3. Устанавливает TZ-переменную окружения и вызывает `tzset()`
 * 4. Регистрирует обработчики событий Wi-Fi и IP
 *
 * @note Вызывайте **до** `app_time_sync_start()`
 */
esp_err_t app_time_sync_init(const char *timezone);

/**
 * @brief Изменение часового пояса на лету (без перезагрузки)
 * @param tz Новый часовой пояс (например, "EST+5EDT")
 * @return
 *        - `ESP_OK` — успешно обновлён и сохранён
 *        - `ESP_FAIL` — ошибка сохранения в NVS
 *
 * @details
 * - Обновляет переменную `s_timezone`
 * - Вызывает `setenv("TZ", tz)` и `tzset()`
 * - Сохраняет новый TZ в NVS (namespace `"time"`, ключ `"tz"`)
 * - **Не требует перезапуска** приложения
 *
 * @warning После вызова время не пересчитывается автоматически — 
 *          текущие `struct tm` остаются неизменными (актуальны для следующих `time()`/`localtime()`).
 */
esp_err_t app_time_sync_set_timezone(const char *tz);

/**
 * @brief Запуск фоновых задач синхронизации времени
 *
 * @details Создаёт две задачи:
 * 1. `time_sync_task` — адаптивная синхронизация при старте
 *    - Если Wi-Fi уже подключён → сразу SNTP
 *    - Иначе → восстанавливает из NVS и ждёт IP_EVENT для SNTP
 * 2. `time_keep_alive_task` — раз в час обновляет timestamp в NVS (если Wi-Fi в сети)
 *
 * @note Вызывайте **после** `app_time_sync_init()`
 */
void app_time_sync_start(void);

/**
 * @brief Деинициализация модуля (остановка задач, освобождение ресурсов)
 *
 * @details
 * - Останавливает SNTP (если запущен)
 * - Удаляет задачи `time_sync_task` и `time_keep_alive_task`
 * - Освобождает обработчики событий (если были динамические)
 * - **Не очищает** NVS — сохранённые данные остаются
 *
 * @note Вызывайте при полном завершении работы приложения (редко требуется)
 */
void app_time_sync_deinit(void);

#endif // APP_TIME_SYNC_H